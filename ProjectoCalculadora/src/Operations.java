public class Operations {

    /**
     * This class is used to perform the basic operations of a calculator
     *
     * @param a the first number introduced by the user
     * @param b the second number introduced by the user
     */

    /*
     * This method is used to divide two numbers
     */
    public static float divi(int a, int b) {
        return (float) a / b;
    }

    /*
     * This method is used to multiply two numbers
     */
    public static int multi(int a, int b) {
        return a * b;
    }

    /*
     * This method is used to add two numbers
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /*
     * This method is used to subtract two numbers
     */
    public static int sub(int a, int b) {
        return a - b;
    }
}
